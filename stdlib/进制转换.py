"""
段言标准库 - 进制转换模块

提供二进制、八进制、十进制、十六进制之间的相互转换。
"""

from typing import Union


Number = Union[int, str]


def 转二进制(数值: Number) -> str:
    """
    将数值转换为二进制字符串

    参数:
        数值: 整数或十进制字符串

    返回:
        二进制字符串（不带前缀）
    """
    if isinstance(数值, str):
        数值 = int(数值)
    return bin(数值)[2:]


def 转八进制(数值: Number) -> str:
    """
    将数值转换为八进制字符串

    参数:
        数值: 整数或十进制字符串

    返回:
        八进制字符串（不带前缀）
    """
    if isinstance(数值, str):
        数值 = int(数值)
    return oct(数值)[2:]


def 转十进制(数值: Number, 原进制: int = 10) -> int:
    """
    将指定进制的数值转换为十进制整数

    参数:
        数值: 数值字符串或整数
        原进制: 原数值的进制（默认 10）

    返回:
        十进制整数
    """
    if isinstance(数值, int):
        return 数值
    return int(str(数值), 原进制)


def 转十六进制(数值: Number, 大写: bool = False) -> str:
    """
    将数值转换为十六进制字符串

    参数:
        数值: 整数或十进制字符串
        大写: 是否使用大写字母（默认 False）

    返回:
        十六进制字符串（不带前缀）
    """
    if isinstance(数值, str):
        数值 = int(数值)
    结果 = hex(数值)[2:]
    if 大写:
        return 结果.upper()
    return 结果


def 二进制转(二进制字符串: str) -> int:
    """
    二进制字符串转十进制整数

    参数:
        二进制字符串: 二进制数字符串

    返回:
        十进制整数
    """
    return int(二进制字符串, 2)


def 八进制转(八进制字符串: str) -> int:
    """
    八进制字符串转十进制整数

    参数:
        八进制字符串: 八进制数字符串

    返回:
        十进制整数
    """
    return int(八进制字符串, 8)


def 十六进制转(十六进制字符串: str) -> int:
    """
    十六进制字符串转十进制整数

    参数:
        十六进制字符串: 十六进制数字符串

    返回:
        十进制整数
    """
    return int(十六进制字符串, 16)


def 任意进制转(数值字符串: str, 原进制: int) -> int:
    """
    任意进制字符串转十进制整数

    参数:
        数值字符串: 数值字符串
        原进制: 原数值的进制（2-36）

    返回:
        十进制整数
    """
    if 原进制 < 2 or 原进制 > 36:
        raise RuntimeError(f"进制必须在 2-36 之间，当前: {原进制}")
    return int(数值字符串, 原进制)


def 转任意进制(数值: Number, 目标进制: int) -> str:
    """
    将十进制数值转换为任意进制字符串

    参数:
        数值: 整数或十进制字符串
        目标进制: 目标进制（2-36）

    返回:
        指定进制的字符串
    """
    if 目标进制 < 2 or 目标进制 > 36:
        raise RuntimeError(f"进制必须在 2-36 之间，当前: {目标进制}")
    if isinstance(数值, str):
        数值 = int(数值)
    if 数值 == 0:
        return '0'
    字符集 = '0123456789abcdefghijklmnopqrstuvwxyz'
    负数 = 数值 < 0
    数值 = abs(数值)
    结果 = ''
    while 数值 > 0:
        结果 = 字符集[数值 % 目标进制] + 结果
        数值 = 数值 // 目标进制
    if 负数:
        结果 = '-' + 结果
    return 结果


