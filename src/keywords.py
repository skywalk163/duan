# =============================================================================
# 辅助函数
# =============================================================================

def is_keyword(word: str) -> bool:
    """判断是否为关键字"""
    return word in ALL_KEYWORDS


def is_double_keyword(word: str) -> bool:
    """判断是否为双字关键字"""
    return word in KEYWORDS_DOUBLE


def get_arity(verb: str) -> int:
    """获取动词的参数数量"""
    return VERB_ARITY.get(verb, 1)


def get_verb_mode(verb: str) -> str:
    """获取动词的修改模式"""
    return VERB_MODE.get(verb, 'functional')


def is_builtin_type(word: str) -> bool:
    """判断是否为内置类型"""
    return word in BUILTIN_TYPES


def normalize_symbol(char: str) -> str:
    """将中文符号转换为英文符号"""
    return SYMBOL_MAP.get(char, char)
